''' ДЗ
Задача 22: Даны два неупорядоченных набора целых чисел (может быть, с повторениями). 
Выдать без повторений в порядке возрастания все те числа, которые встречаются в обоих наборах.
Пользователь вводит 2 числа. n - кол-во элементов первого множества. m - кол-во элементов второго множества. 
Затем пользователь вводит сами элементы множеств.
'''

# Ввод количества элементов для каждого множества
n = int(input("Введите количество элементов первого множества: "))
m = int(input("Введите количество элементов второго множества: "))

# Ввод элементов для каждого множества
set1 = set()
set2 = set()

print("Введите элементы первого множества через Enter:")
for _ in range(n):
    element = int(input())
    set1.add(element)

print("Введите элементы второго множества через Enter:")
for _ in range(m):
    element = int(input())
    set2.add(element)

# Вычисление пересечения множеств и сортировка уникальных элементов
result_set = sorted(set1.intersection(set2))

# Вывод результата
print("Элементы, встречающиеся в обоих множествах в порядке возрастания:")
print(result_set)


# Данные
var2 = '1 3 5 7 9'
var3 = '2 3 4 5'

# Преобразование строк в списки чисел
list_var2 = list(map(int, var2.split()))    # map(int, ... ): Эта функция применяет функцию int к каждому элементу 
                                            # списка, преобразуя строки (которые были получены после split) 
                                            # в целые числа.
list_var3 = list(map(int, var3.split()))    # .split - Этот метод разделяет строку var3 на подстроки, 
                                            # используя пробел в качестве разделителя, и возвращает список строк

# Нахождение пересекающихся элементов
intersection = sorted(set(list_var2) & set(list_var3))

# Вывод результата
if intersection:
    print(f"Пересекающиеся элементы в порядке возрастания: {intersection}")
else:
    print("Нет пересекающихся элементов.")


